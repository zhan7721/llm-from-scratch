"""Online DPO: generating preference pairs on-the-fly from the current policy.

This module implements Online DPO, which differs from offline DPO in that
preference pairs are generated on-the-fly from the current policy, rather
than using a static dataset. This addresses a key limitation of offline DPO:
the distribution mismatch between the data-generating policy and the policy
being trained.

Components:
- generate_and_score: generate responses from current policy, score with
  reward model, select best/worst as chosen/rejected
- OnlineDPODataset: dynamically created preference pairs from online gen
- OnlineDPOTrainer: generates responses online, then trains with DPO

Why Online DPO?
---------------
In standard (offline) DPO, preference pairs are collected once and reused.
This causes a distribution shift: the data was generated by an older version
of the policy, but we are training the current version. Online DPO fixes this
by generating fresh preference data from the current policy each epoch.

The trade-off is computational cost: generating responses is expensive.
But the quality of training signal is much better because the preference
pairs reflect the current policy's behavior.

Reference: "Direct Preference Optimization: Your Language Model is Secretly
a Reward Model" (Rafailov et al., 2023). Online DPO variant inspired by
"Self-Play Fine-Tuning Converts Weak Language Models to Strong Language
Models" (Chen et al., 2024).
"""

import sys
import os
import torch
import torch.nn as nn
import torch.nn.functional as F
from typing import Dict, List, Optional, Tuple
from torch.utils.data import Dataset

# Import DPO components from the previous chapter
